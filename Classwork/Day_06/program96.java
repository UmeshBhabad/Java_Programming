// Dynamic Array Declaration and Initialization

// main class

class program96
{
    public static void main(String args[])          // Main Method
    {
        int Arr[] = new int[4];                     // Array Declaration with size 3

        Arr[0] = 10;                                // Array Initialization
        Arr[1] = 20;
        Arr[2] = 30;
        Arr[3] = 40;

        System.out.println(Arr[0]);                 // Printing array Elements
        System.out.println(Arr[1]);
        System.out.println(Arr[2]);
        System.out.println(Arr[3]);
    }
}